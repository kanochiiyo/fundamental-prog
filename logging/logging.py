# Logging -> proses menuliskan apa yang terjadi pada program. Jenis logging: 
# 1. Debug: informasi yang ditulis untuk memahami variabel ada yang dilempar
# 2. Info: informasi yang ditulis untuk memahami pada saat itu proses apa yang berjalan
# 3. Error: informasi yang ditulis untuk memahami error apa yang terjadi.

